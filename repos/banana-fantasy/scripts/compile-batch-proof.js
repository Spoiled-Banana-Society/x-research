/**
 * Compiles contracts/BBB4BatchProof.sol via solc-js and writes the bytecode
 * + ABI to lib/contracts/bbb4BatchProofArtifact.ts so the deploy endpoint
 * can ship them inline. Run once after editing the contract:
 *
 *   node scripts/compile-batch-proof.js
 *
 * Idempotent — overwrites the artifact file with byte-equivalent output if
 * the contract source hasn't changed.
 */

const fs = require('node:fs');
const path = require('node:path');
const solc = require('solc');

const TARGETS = [
  {
    sourceFile: 'BBB4BatchProof.sol',
    contractName: 'BBB4BatchProof',
    outRel: 'lib/contracts/bbb4BatchProofArtifact.ts',
    bytecodeConst: 'BBB4_BATCH_PROOF_BYTECODE',
    abiConst: 'BBB4_BATCH_PROOF_ABI',
  },
  {
    sourceFile: 'BBB4BatchProofVRF.sol',
    contractName: 'BBB4BatchProofVRF',
    outRel: 'lib/contracts/bbb4BatchProofVRFArtifact.ts',
    bytecodeConst: 'BBB4_BATCH_PROOF_VRF_BYTECODE',
    abiConst: 'BBB4_BATCH_PROOF_VRF_ABI',
  },
  {
    sourceFile: 'BBB4BatchProofVRFCommit.sol',
    contractName: 'BBB4BatchProofVRFCommit',
    outRel: 'lib/contracts/bbb4BatchProofVRFCommitArtifact.ts',
    bytecodeConst: 'BBB4_BATCH_PROOF_VRF_COMMIT_BYTECODE',
    abiConst: 'BBB4_BATCH_PROOF_VRF_COMMIT_ABI',
  },
];

for (const t of TARGETS) {
  const sourcePath = path.join(__dirname, '..', 'contracts', t.sourceFile);
  if (!fs.existsSync(sourcePath)) {
    console.warn(`! skipping ${t.sourceFile} — file not found`);
    continue;
  }
  const source = fs.readFileSync(sourcePath, 'utf8');

  const input = {
    language: 'Solidity',
    sources: { [t.sourceFile]: { content: source } },
    settings: {
      optimizer: { enabled: true, runs: 200 },
      outputSelection: { '*': { '*': ['abi', 'evm.bytecode.object'] } },
    },
  };

  const output = JSON.parse(solc.compile(JSON.stringify(input)));

  if (output.errors) {
    const fatal = output.errors.filter((e) => e.severity === 'error');
    if (fatal.length > 0) {
      fatal.forEach((e) => console.error(e.formattedMessage));
      process.exit(1);
    }
    output.errors.forEach((e) => console.warn(e.formattedMessage));
  }

  const compiled = output.contracts[t.sourceFile][t.contractName];
  if (!compiled) throw new Error(`Compiled output for ${t.contractName} not found`);

  const bytecode = '0x' + compiled.evm.bytecode.object;
  const abi = compiled.abi;

  const ts = `// AUTO-GENERATED by scripts/compile-batch-proof.js — do not hand-edit.
// Regenerate after editing contracts/${t.sourceFile}.

export const ${t.bytecodeConst} = ${JSON.stringify(bytecode)} as const;

export const ${t.abiConst} = ${JSON.stringify(abi, null, 2)} as const;
`;

  const outPath = path.join(__dirname, '..', t.outRel);
  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, ts);

  console.log(`✓ Compiled ${t.contractName}`);
  console.log(`  bytecode: ${bytecode.length} chars (${Math.round(bytecode.length / 2 - 1)} bytes)`);
  console.log(`  abi: ${abi.length} entries`);
  console.log(`  → ${t.outRel}`);
}
