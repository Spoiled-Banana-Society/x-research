/**
 * Compiles contracts/BBB4BatchProof.sol via solc-js and writes the bytecode
 * + ABI to lib/contracts/bbb4BatchProofArtifact.ts so the deploy endpoint
 * can ship them inline. Run once after editing the contract:
 *
 *   node scripts/compile-batch-proof.js
 *
 * Idempotent — overwrites the artifact file with byte-equivalent output if
 * the contract source hasn't changed.
 */

const fs = require('node:fs');
const path = require('node:path');
const solc = require('solc');

const CONTRACT_PATH = path.join(__dirname, '..', 'contracts', 'BBB4BatchProof.sol');
const OUT_PATH = path.join(__dirname, '..', 'lib', 'contracts', 'bbb4BatchProofArtifact.ts');
const CONTRACT_NAME = 'BBB4BatchProof';

const source = fs.readFileSync(CONTRACT_PATH, 'utf8');

const input = {
  language: 'Solidity',
  sources: {
    'BBB4BatchProof.sol': { content: source },
  },
  settings: {
    optimizer: { enabled: true, runs: 200 },
    outputSelection: {
      '*': {
        '*': ['abi', 'evm.bytecode.object'],
      },
    },
  },
};

const output = JSON.parse(solc.compile(JSON.stringify(input)));

if (output.errors) {
  const fatal = output.errors.filter((e) => e.severity === 'error');
  if (fatal.length > 0) {
    fatal.forEach((e) => console.error(e.formattedMessage));
    process.exit(1);
  }
  output.errors.forEach((e) => console.warn(e.formattedMessage));
}

const compiled = output.contracts['BBB4BatchProof.sol'][CONTRACT_NAME];
if (!compiled) throw new Error(`Compiled output for ${CONTRACT_NAME} not found`);

const bytecode = '0x' + compiled.evm.bytecode.object;
const abi = compiled.abi;

const ts = `// AUTO-GENERATED by scripts/compile-batch-proof.js — do not hand-edit.
// Regenerate after editing contracts/BBB4BatchProof.sol.

export const BBB4_BATCH_PROOF_BYTECODE = ${JSON.stringify(bytecode)} as const;

export const BBB4_BATCH_PROOF_ABI = ${JSON.stringify(abi, null, 2)} as const;
`;

fs.mkdirSync(path.dirname(OUT_PATH), { recursive: true });
fs.writeFileSync(OUT_PATH, ts);

console.log(`✓ Compiled ${CONTRACT_NAME}`);
console.log(`  bytecode: ${bytecode.length} chars (${Math.round(bytecode.length / 2 - 1)} bytes)`);
console.log(`  abi: ${abi.length} entries`);
console.log(`  → ${path.relative(process.cwd(), OUT_PATH)}`);
